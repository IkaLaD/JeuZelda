package universite_paris8.iut.EtrangeEtrange.modele.Utilitaire;

public class Coordonnee
{
    private double x,y;

    public Coordonnee(double x,double y)
    {
        this.x = x;
        this.y = y;
    }

    public double getX()
    {
        return this.x;
    }

    public double getY()
    {
        return this.y;
    }
}
